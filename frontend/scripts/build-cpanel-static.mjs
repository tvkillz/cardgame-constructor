#!/usr/bin/env node
/**
 * Build a static folder for cPanel public_html (no Node.js app).
 * Output: frontend/dist-cpanel-static/{project}-static/
 *
 * Usage (from frontend/):
 *   npm run build:cpanel-static
 *   PROJECT=voidborn npm run build:cpanel-static
 */
import { execSync } from 'node:child_process'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

import { buildPaths, resolveProjectId } from './project-paths.mjs'
import { staticExportDir } from './project-next.mjs'

const frontendRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const projectId = resolveProjectId()
const distName = `${projectId}-static`
const deployRoot = path.join(frontendRoot, 'dist-cpanel-static', distName)

function rmrf(dir) {
  if (fs.existsSync(dir)) fs.rmSync(dir, { recursive: true, force: true })
}

function copyTree(src, dest) {
  fs.mkdirSync(path.dirname(dest), { recursive: true })
  fs.cpSync(src, dest, { recursive: true })
}

function dirSizeBytes(dir) {
  let total = 0
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) total += dirSizeBytes(full)
    else total += fs.statSync(full).size
  }
  return total
}

function formatMb(bytes) {
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`
}

function writeHtaccess(targetRoot) {
  const contents = `# Generated by build:cpanel-static — Apache on cPanel
<IfModule mod_rewrite.c>
RewriteEngine On
RewriteBase /

# Vite play app — client-side routes under /play/
RewriteCond %{REQUEST_URI} ^/play/
RewriteCond %{REQUEST_FILENAME} !-f
RewriteCond %{REQUEST_FILENAME} !-d
RewriteRule ^play/(.*)$ /play/index.html [L]
</IfModule>
`
  fs.writeFileSync(path.join(targetRoot, '.htaccess'), contents)
}

function maybeZipDir(sourceDir, zipPath) {
  if (!fs.existsSync(sourceDir)) return false
  try {
    const parent = path.dirname(sourceDir)
    const base = path.basename(sourceDir)
    execSync(`zip -rq ${JSON.stringify(zipPath)} ${JSON.stringify(base)}`, {
      cwd: parent,
      stdio: 'inherit',
    })
    return true
  } catch {
    console.warn('[build:cpanel-static] zip not available — upload folder manually')
    return false
  }
}

console.log(`[build:cpanel-static] project=${projectId}`)

execSync('node scripts/build-prod.mjs --skip-web', {
  cwd: frontendRoot,
  stdio: 'inherit',
  env: { ...process.env, PROJECT: projectId },
})

execSync('node scripts/build-web-static.mjs', {
  cwd: frontendRoot,
  stdio: 'inherit',
  env: { ...process.env, PROJECT: projectId },
})

const exportDir = staticExportDir(projectId, frontendRoot)
if (!fs.existsSync(path.join(exportDir, 'index.html'))) {
  console.error(`[build:cpanel-static] Missing ${path.join(exportDir, 'index.html')}`)
  process.exit(1)
}

rmrf(deployRoot)
fs.mkdirSync(path.dirname(deployRoot), { recursive: true })
copyTree(exportDir, deployRoot)

const playSrc = buildPaths(projectId).play
copyTree(playSrc, path.join(deployRoot, 'play'))
writeHtaccess(deployRoot)

const readmeSrc = path.join(frontendRoot, 'deploy/cpanel/STATIC.md')
if (fs.existsSync(readmeSrc)) {
  fs.copyFileSync(readmeSrc, path.join(deployRoot, 'SETUP.md'))
}

const distRoot = path.dirname(deployRoot)
const zipPath = path.join(distRoot, `${distName}.zip`)
rmrf(zipPath)
const zipped =
  process.env.CPANEL_STATIC_NO_ZIP === '1' ? false : maybeZipDir(deployRoot, zipPath)

console.log('')
console.log('[build:cpanel-static] Done.')
console.log(`  Folder: ${deployRoot} (${formatMb(dirSizeBytes(deployRoot))})`)
if (zipped) {
  console.log(`  Zip:    ${zipPath} (${formatMb(fs.statSync(zipPath).size)})`)
}
console.log('  Upload to cPanel public_html (or CPANEL_STATIC_FTP_REMOTE_DIR).')
console.log('  No Node.js app or NPM Install required.')
console.log('  See SETUP.md in the output folder.')

if (process.argv.includes('--upload')) {
  execSync('node scripts/upload-cpanel-static.mjs', {
    cwd: frontendRoot,
    stdio: 'inherit',
    env: { ...process.env, PROJECT: projectId },
  })
}
